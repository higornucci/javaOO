package br.com.htcursos.aula16;

public class Soma {
	public double calcular(double valorEsquerda, double valorDireita) {
		return valorEsquerda + valorDireita;
	}
	
	public int calcular(int valorEsquerda, int valorDireita) {
		return valorEsquerda + valorDireita;
	}
	
	public float calcular(float valorEsquerda, float valorDireita) {
		return valorEsquerda + valorDireita;
	}
}
